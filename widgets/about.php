<p>WordPress is already a powerful website management system that not
only allows you to manage your content, but have others interact as
well. Most projects rely on several pieces of software. Perhaps you use
WordPress as a development blog, Salesforce for CRM, and some other
software for time tracking and project management. Propel aims to
combine all of these into a single interface that is already friendly
and comfortable - WordPress.</p>

<p>In it's short lifetime, Propel provides many of the capabilities
you'd expect to see from any standard project management solution.
Currently, these capabilities include the following:</p>
<br>

<ul>
	<li>
		<p>- Ability to create multiple projects</p>
	</li>
	
	<li>
		<p>- Ability to create tasks</p>
	</li>
	
	<li>
		<p>- Track tasks by project</p>
	</li>
	
	<li>
		<p>- Assign users to a task</p>
	</li>
	
	<li>
		<p>- A front-end visual interface</p>
	</li>
	
	<li>
		<p>- A bug reporting system</p>
	</li>
</ul><br>

<p>It is highly anticipated this list will expand over time and under
the direction of its users. If there is a feature you would like to see
in the next version of Propel please request it below.</p>