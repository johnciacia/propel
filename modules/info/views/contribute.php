<p><strong>Documentation</strong><br />
What good is software if no one knows how to use it? The user interfaces
are designed to be as intuitive as possible, and the code is written to
be as clear and concise as possible. However, what may be intuitive,
clear, and concise to one, may not be to others. Moreover, users often
find ways to use software in ways the designer never imagined. If you
thought something was particularly difficult, or have done something
unique, blog about it, and I will repost it here with a backlink to your
site.<br />
<br />
You can also help by translating the project into other languages.</p>

